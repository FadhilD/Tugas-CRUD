    private void CariData() {
    try {
        st = conn.createStatement();
        String columnName = cmbcari.getSelectedItem().toString();
        String searchTerm = txtcari.getText();
        String query = "SELECT * FROM dataperpus WHERE " + columnName + " LIKE '%" + searchTerm + "%'";
        rs = st.executeQuery(query);

        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("id");
        model.addColumn("judul");
        model.addColumn("genre");
        model.addColumn("penulis");
        model.addColumn("penerbit");
        model.addColumn("lokasi");
        model.addColumn("stock");

        // Clear existing table data
        model.setRowCount(0);

        boolean foundData = false;
        while (rs.next()) {
            foundData = true;
            Object[] data = {
                rs.getString("id"),
                rs.getString("judul"),
                rs.getString("genre"),
                rs.getString("penulis"),
                rs.getString("penerbit"),
                rs.getString("lokasi"),
                rs.getString("stock"),
            };
            model.addRow(data);
        }

        if (!foundData) {
            JOptionPane.showMessageDialog(this, "Data tidak ditemukan", "Pencarian", JOptionPane.INFORMATION_MESSAGE);
        }

        // Set model to the table outside of the loop
        jTable1.setModel(model);
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat melakukan pencarian", "Error", JOptionPane.ERROR_MESSAGE);
    }
}
    
    private void TampilData(){
        try {
            st=conn.createStatement();
            rs=st.executeQuery("SELECT * FROM dataperpus");
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("id");
            model.addColumn("judul");
            model.addColumn("genre");
            model.addColumn("penulis");
            model.addColumn("penerbit");
            model.addColumn("lokasi");
            model.addColumn("stock");
            
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            model.setRowCount(0);
            while (rs.next()) {
                Object[] data = {
                    rs.getString("id"),
                    rs.getString("judul"),
                    rs.getString("genre"),
                    rs.getString("penulis"),
                    rs.getString("penerbit"),
                    rs.getString("lokasi"),
                    rs.getString("stock"),
                };
                model.addRow(data);
                jTable1.setModel(model);
                
            }
        } catch (Exception e) {
        }
    }
    
