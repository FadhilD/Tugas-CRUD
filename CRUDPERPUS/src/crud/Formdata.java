    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
        // TODO add your handling code here:
        try {
            st = conn.createStatement();
            if(txtjudul.getText().equals("") || 
                    txtpenulis.getText().equals("") ||
                    txtpenerbit.getText().equals("") ||
                    txtlokasi.getText().equals("") ||
                    txtgenre.getText().equals("") ||
                    txtstock.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Data Tidak Boleh Kosong", "Validasi Data", JOptionPane.ERROR_MESSAGE);
            return;
            }
            //aksi tambah
            if(btntambah.getText() == "TAMBAH"){
               String sql = "INSERT INTO dataperpus(judul, genre, penulis, penerbit, lokasi, stock) VALUES ('"+txtjudul.getText() +
                       "','" + txtgenre.getText() +
                       "','" + txtpenulis.getText() +
                       "','" + txtpenerbit.getText() +
                       "','" + txtlokasi.getText() +
                       "','" + txtstock.getText() + "')";  
               st.executeUpdate(sql);
               JOptionPane.showMessageDialog(null, "Data Berhasil di Simpan");
               Bersih();
               TampilData();
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btntambahActionPerformed

